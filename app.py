@st.cache_data(ttl=300, show_spinner=False)
def fetch(ticker, period="1y", start=None, end=None):
    try:
        interval_map = {"1d": "5m", "5d": "15m"}
        kw = dict(progress=False, auto_adjust=True)
        if start and end:
            df = yf.download(ticker, start=start, end=end, **kw)
        else:
            df = yf.download(ticker, period=period,
                             interval=interval_map.get(period, "1d"), **kw)
        if df is None or df.empty:
            return None
        if isinstance(df.columns, pd.MultiIndex):
            df.columns = [c[0] for c in df.columns]
        df = df.dropna(how="all")
        return df if len(df) >= 5 else None
    except Exception:
        return None


def indicators(df):
    df = df.copy()
    c = df["Close"].squeeze()
    v = df["Volume"].squeeze() if "Volume" in df.columns else pd.Series(0, index=df.index)

    df["MA20"]  = c.rolling(20).mean()
    df["MA50"]  = c.rolling(50).mean()
    df["MA200"] = c.rolling(200).mean()
    std20 = c.rolling(20).std()
    df["BB_u"] = df["MA20"] + 2 * std20
    df["BB_l"] = df["MA20"] - 2 * std20

    delta = c.diff()
    gain  = delta.clip(lower=0).rolling(14).mean()
    loss  = (-delta.clip(upper=0)).rolling(14).mean()
    df["RSI"] = 100 - 100 / (1 + gain / (loss + 1e-9))

    e12 = c.ewm(span=12).mean()
    e26 = c.ewm(span=26).mean()
    df["MACD"]     = e12 - e26
    df["MACD_sig"] = df["MACD"].ewm(span=9).mean()
    df["MACD_h"]   = df["MACD"] - df["MACD_sig"]

    df["VolMA"] = v.rolling(20).mean()
    h = df["High"].squeeze(); l = df["Low"].squeeze()
    tr = pd.concat([(h - l), (h - c.shift()).abs(), (l - c.shift()).abs()], axis=1).max(axis=1)
    df["ATR"] = tr.rolling(14).mean()
    return df


