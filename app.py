def compute_signal(df):
    sigs = []
    c    = df["Close"].squeeze()
    last = float(c.iloc[-1])

    if "RSI" in df.columns:
        rsi = float(df["RSI"].iloc[-1])
        if not np.isnan(rsi):
            if rsi < 35:
                sigs.append(("RSI Oversold",   "BUY",  0.8, f"RSI {rsi:.1f} < 35"))
            elif rsi > 70:
                sigs.append(("RSI Overbought", "SELL", 0.8, f"RSI {rsi:.1f} > 70"))
            else:
                sigs.append(("RSI Neutral",    "HOLD", 0.5, f"RSI {rsi:.1f}"))

    if "MA20" in df.columns and "MA50" in df.columns:
        m20 = float(df["MA20"].iloc[-1]); m50 = float(df["MA50"].iloc[-1])
        p20 = float(df["MA20"].iloc[-5]); p50 = float(df["MA50"].iloc[-5])
        if not (np.isnan(m20) or np.isnan(m50)):
            if m20 > m50 and p20 <= p50:
                sigs.append(("Golden Cross",        "BUY",  0.9, "MA20 crossed above MA50"))
            elif m20 < m50 and p20 >= p50:
                sigs.append(("Death Cross",         "SELL", 0.9, "MA20 crossed below MA50"))
            elif last > m20 > m50:
                sigs.append(("Above both MAs",      "BUY",  0.7, f"{last:.1f} > MA20 {m20:.1f} > MA50 {m50:.1f}"))
            elif last < m20 < m50:
                sigs.append(("Below both MAs",      "SELL", 0.7, f"{last:.1f} < MA20 {m20:.1f} < MA50 {m50:.1f}"))

    if "MACD" in df.columns and "MACD_sig" in df.columns:
        mv  = float(df["MACD"].iloc[-1]);     sv  = float(df["MACD_sig"].iloc[-1])
        mv2 = float(df["MACD"].iloc[-2]);     sv2 = float(df["MACD_sig"].iloc[-2])
        if not (np.isnan(mv) or np.isnan(sv)):
            if mv > sv and mv2 <= sv2:
                sigs.append(("MACD Bullish Crossover", "BUY",  0.75, f"MACD {mv:.2f} crossed Signal {sv:.2f}"))
            elif mv < sv and mv2 >= sv2:
                sigs.append(("MACD Bearish Crossover", "SELL", 0.75, f"MACD {mv:.2f} crossed Signal {sv:.2f}"))
            elif mv > 0:
                sigs.append(("MACD Positive Zone",     "BUY",  0.55, f"MACD={mv:.2f}"))

    if "BB_u" in df.columns and "BB_l" in df.columns:
        bbu = float(df["BB_u"].iloc[-1]); bbl = float(df["BB_l"].iloc[-1])
        if not (np.isnan(bbu) or np.isnan(bbl)):
            if last > bbu:
                sigs.append(("BB Upper Breakout", "SELL", 0.7, f"{last:.1f} > Upper {bbu:.1f}"))
            elif last < bbl:
                sigs.append(("BB Lower Bounce",   "BUY",  0.7, f"{last:.1f} < Lower {bbl:.1f}"))

    if not sigs:
        return "HOLD", 0.5, []
    bs = sum(conf for _, s, conf, _ in sigs if s == "BUY")
    ss = sum(conf for _, s, conf, _ in sigs if s == "SELL")
    if bs > ss * 1.3:
        return "BUY",  bs / (bs + ss + 0.01), sigs
    elif ss > bs * 1.3:
        return "SELL", ss / (bs + ss + 0.01), sigs
    return "HOLD", 0.5, sigs


def action_text(sig, last, conf, cur):
    if sig == "BUY":
        entry = last * (1 - 0.01 * (1 - conf))
        tp    = last * 1.03
        sl    = last * 0.985
        return (f"Entry ≤ {cur}{entry:,.2f}  |  "
                f"Target {cur}{tp:,.2f} (+3.0%)  |  "
                f"Stop-loss {cur}{sl:,.2f} (−1.5%)  |  "
                f"Conf: {conf*100:.0f}%")
    elif sig == "SELL":
        entry = last * (1 + 0.01 * (1 - conf))
        tp    = last * 0.97
        sl    = last * 1.015
        return (f"Exit ≥ {cur}{entry:,.2f}  |  "
                f"Target {cur}{tp:,.2f} (−3.0%)  |  "
                f"Stop-loss {cur}{sl:,.2f} (+1.5%)  |  "
                f"Conf: {conf*100:.0f}%")
    else:
        lo = last * 0.98; hi = last * 1.02
        return f"Hold between {cur}{lo:,.2f} – {cur}{hi:,.2f}  (±2% range)  |  Conf: {conf*100:.0f}%"


def next_day_pred(df, preds):
    c    = df["Close"].squeeze()
    o    = df["Open"].squeeze()
    oc_r = float((o / c).mean())
    d1   = [float(v[0]) for v in preds.values() if v is not None and len(v) > 0]
    if not d1:
        return None, None
    nc = float(np.mean(d1))
    return nc * oc_r, nc


