def pred_lstm(c, days):
    scaler = MinMaxScaler()
    vals   = c.values.reshape(-1, 1)
    sc     = scaler.fit_transform(vals)
    win    = min(60, len(sc) // 2)
    if win < 10:
        return None
    X, y = zip(*[(sc[i-win:i, 0], sc[i, 0]) for i in range(win, len(sc))])
    X, y = np.array(X), np.array(y)
    sp   = int(len(X) * 0.85)
    m    = GradientBoostingRegressor(n_estimators=200, max_depth=4,
                                     learning_rate=0.05, subsample=0.8, random_state=42)
    m.fit(X[:sp], y[:sp])
    buf  = sc[-win:].flatten()
    out  = []
    for _ in range(days):
        p = m.predict(buf.reshape(1, -1))[0]
        out.append(p)
        buf = np.append(buf[1:], p)
    return scaler.inverse_transform(np.array(out).reshape(-1, 1)).flatten()


def pred_rf(df, days):
    c = df["Close"].squeeze()
    feat = pd.DataFrame(index=df.index)
    for lag in [1, 2, 3, 5, 10, 20]:
        feat[f"l{lag}"] = c.shift(lag)
    for w in [5, 20, 50]:
        feat[f"ma{w}"] = c.rolling(w).mean()
        feat[f"sd{w}"] = c.rolling(w).std()
    for col in ["RSI", "MACD", "ATR"]:
        feat[col] = df.get(col, pd.Series(0, index=df.index))
    feat = feat.dropna()
    y    = c.loc[feat.index].values
    X    = feat.values
    sp   = int(len(X) * 0.85)
    if sp < 10:
        return None
    m = RandomForestRegressor(n_estimators=150, max_depth=8, random_state=42, n_jobs=-1)
    m.fit(X[:sp], y[:sp])
    row  = feat.iloc[-1].copy()
    out  = []
    for _ in range(days):
        p = m.predict(row.values.reshape(1, -1))[0]
        out.append(p)
        for lag in [20, 10, 5, 3, 2]:
            k1, k2 = f"l{lag}", f"l{lag-1}"
            if k1 in row and k2 in row:
                row[k1] = row[k2]
        row["l1"] = p
    return np.array(out)


def pred_linear(c, days):
    X = np.arange(len(c)).reshape(-1, 1)
    m = LinearRegression().fit(X, c.values)
    return m.predict(np.arange(len(c), len(c) + days).reshape(-1, 1))


def pred_xgb(df, days):
    c    = df["Close"].squeeze().values
    n    = min(30, len(c))
    trend = np.polyfit(np.arange(n), c[-n:], 1)[0]
    mean  = c[-n:].mean()
    price = c[-1]
    out   = []
    for i in range(days):
        price = price + trend * (0.85 ** i) + (mean - price) * 0.03
        out.append(price)
    return np.array(out)


@st.cache_data(ttl=600, show_spinner=False)
def run_preds(ticker, days=30):
    df = fetch(ticker, period="2y")
    if df is None or len(df) < 80:
        return {}
    df  = indicators(df)
    c   = df["Close"].squeeze()
    out = {}
    try:
        p = pred_lstm(c, days)
        if p is not None:
            out["LSTM (GBM)"] = p
    except Exception:
        pass
    try:
        p = pred_rf(df, days)
        if p is not None:
            out["Random Forest"] = p
    except Exception:
        pass
    try:
        out["Linear Trend"]  = pred_linear(c, days)
    except Exception:
        pass
    try:
        out["XGBoost-style"] = pred_xgb(df, days)
    except Exception:
        pass
    return out


